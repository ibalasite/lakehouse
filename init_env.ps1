Write-Host "╠══════════════════════════════════════════════════════╣"
Write-Host "║  Airflow        http://localhost:30888"
Write-Host ("║    user: {0,-44}║" -f "admin")
Write-Host ("║    pass: {0,-44}║" -f $AirflowPass)
Write-Host "╠══════════════════════════════════════════════════════╣"
Write-Host "║  Metabase       http://localhost:30300"
Write-Host ("║    user: {0,-44}║" -f "admin@local.com")
