    "MINIO_ROOT_USER=$MinioUser",
    "MINIO_ROOT_PASSWORD=$MinioPass",
    "MINIO_API_PORT=9000",
    "MINIO_CONSOLE_PORT=9001",
    "MINIO_BUCKET=lakehouse",
    "",
    "# -- Apache Polaris ----------------------------------",
    "POLARIS_PORT=8181",
    "POLARIS_MGMT_PORT=8182",
    "POLARIS_REALM=POLARIS",
    "POLARIS_CLIENT_ID=$PolarisId",
    "POLARIS_CLIENT_SECRET=$PolarisSecret",
    "POLARIS_CATALOG=lakehouse",
    "",
    "# -- Trino -------------------------------------------",
    "TRINO_PORT=8080",
    "",
    "# -- MySQL -------------------------------------------",
    "MYSQL_ROOT_PASSWORD=$MysqlRoot",
    "MYSQL_DATABASE=lakehouse_cache",
    "MYSQL_USER=lakehouse",
    "MYSQL_PASSWORD=$MysqlPass",
    "MYSQL_PORT=3306",
    "",
    "# -- PostgreSQL (Airflow backend) --------------------",
    "POSTGRES_USER=airflow",
    "POSTGRES_PASSWORD=$PostgresPass",
    "POSTGRES_DB=airflow",
    "POSTGRES_PORT=5432",
    "",
    "# -- Airflow -----------------------------------------",
    "AIRFLOW_PORT=8888",
    "AIRFLOW_ADMIN_USER=admin",
    "AIRFLOW_ADMIN_PASSWORD=$AirflowPass",
    "AIRFLOW__CORE__FERNET_KEY=$FernetKey",
    "AIRFLOW__DATABASE__SQL_ALCHEMY_CONN=postgresql+psycopg2://airflow:${PostgresPass}@postgres:5432/airflow",
    "AIRFLOW__CORE__EXECUTOR=LocalExecutor",
    "AIRFLOW__CORE__LOAD_EXAMPLES=False",
    "",
    "# -- Metabase ----------------------------------------",
    "METABASE_PORT=3000",
    "METABASE_URL=http://localhost:3000",
    "METABASE_ADMIN_EMAIL=admin@local.com",
    "METABASE_ADMIN_PASSWORD=$MetabasePass",
    "",
    "# -- 時區（所有服務共用） -------------------------",
    "# 修改此處即可統一調整所有服務的時區",
    "TZ=Asia/Taipei"
) -join "`n"

[System.IO.File]::WriteAllText($EnvFile, $envContent + "`n")

