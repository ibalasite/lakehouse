#!/usr/bin/env pwsh
# init_env.ps1 — Generate fresh random credentials and write to .env (Windows)
# Usage:  .\init_env.ps1
# Equivalent to init_env.sh on macOS/Linux.

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

$ScriptDir = $PSScriptRoot
$EnvFile   = Join-Path $ScriptDir '.env'

# ── Random generators ──────────────────────────────────────────────────────────
function New-RandomHex([int]$bytes) {
    $buf = [System.Security.Cryptography.RandomNumberGenerator]::GetBytes($bytes)
    return ($buf | ForEach-Object { '{0:x2}' -f $_ }) -join ''
}

function New-RandomPass([int]$len) {
    $chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789'.ToCharArray()
    $buf   = [System.Security.Cryptography.RandomNumberGenerator]::GetBytes($len)
    return -join ($buf | ForEach-Object { $chars[$_ % $chars.Length] })
}

function New-FernetKey() {
    # Try python3, then python, then fall back to raw base64
    foreach ($py in @('python3', 'python')) {
        if (Get-Command $py -ErrorAction SilentlyContinue) {
            $key = & $py -c "from cryptography.fernet import Fernet; print(Fernet.generate_key().decode())" 2>$null
            if ($LASTEXITCODE -eq 0 -and $key) { return $key.Trim() }
        }
    }
    # Fallback: 32 random bytes as base64
    $buf = [System.Security.Cryptography.RandomNumberGenerator]::GetBytes(32)
    return [System.Convert]::ToBase64String($buf)
}

# ── Generate credentials ───────────────────────────────────────────────────────
$MinioUser     = "minio_$(New-RandomHex 4)"
$MinioPass     = New-RandomPass 24
$PolarisId     = "polaris_$(New-RandomHex 4)"
$PolarisSecret = New-RandomPass 32
$MysqlRoot     = New-RandomPass 24
$MysqlPass     = New-RandomPass 24
$PostgresPass  = New-RandomPass 24
$AirflowPass   = New-RandomPass 20
$FernetKey     = New-FernetKey
$MetabasePass  = New-RandomPass 20

# ── Write .env (LF line endings for cross-platform kubectl compatibility) ──────
$timestamp  = (Get-Date).ToUniversalTime().ToString('yyyy-MM-dd HH:mm:ss UTC')
$envContent = (
